%            Qual a diferença entre um vetor e um escalar? 
%            Dê pelo menos três exemplos de cada.

% Resposta:

% Diferença fundamental entre um vetor e um escalar é "direção".
% Um escalar é uma grandeza só valor que não existe direção,é unidimensional.
% 
% Por exemplo:Temperatura,Massa,Distância.

%E um vetor é uma grandeza que existe valor e direção,é multidimensional(pode ser bidimensional ou tridimensional).
%Por exemplo:Força,Velocidade,Campo Magnético.