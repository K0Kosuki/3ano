public class Pontos {
    private double x, y;

    Pontos(double x, double y) {
        this.x = x;
        this.y = y;
    }

    double getX() {
        return this.x;
    }
    double setX(double x) {
       return this.x = x;
    }
    double getY() {
        return this.y;
    }
    double setY(double y) {
       return this.y = y;
    }

   
}