public class Contratavel {
    private String cliente;
    private double peso; 
    private double distancia;

public Contratavel(String cli, double p,double dis){
    this.cliente=cli;
    this.peso=p;
    this.distancia=dis;
}
public String getCliente(){
    return this.cliente;
}
public double getPeso(){
    return getPeso();
}
public double getDistancia(){
    return getDistancia();
}



}
